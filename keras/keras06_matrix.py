# keras06_matrix.py

import numpy as np

x1 = np.array([1,2,3])
print('x1 =', x1.shape)     # x1 = (3,) : 스칼라가 3개인 '벡터'

x2 = np.array([[1,2,3],[4,5,6]])
print('x2 =', x2.shape)     # x2 = (2, 3) : 2행 3열 '행렬'

x3 = np.array([[[1,2,3],[4,5,6]],[[5,6,7],[8,9,10]]])
print('x3 =', x3.shape)     # x3 = (2, 2, 3) : 2x2x3 '텐서'

x4 = np.array([[[[1,2,3],[4,5,6]],[[5,6,7],[8,9,10]]]])
print('x4 =', x4.shape)     # x4 = (1, 2, 2, 3) : 1x2x2x3 '텐서'

x5 = np.array([[[[1]]],[[[2]]]])
print('x5 =', x5.shape)     # x5 = (2, 1, 1, 1) : 2x1x1x1 '텐서'

x6 = np.array([[[[1,2,3,4,5],[7,8,9,10,11]],[[11,12,13,14,15],[17,18,19,20,21]]],
                [[[1,2,3,4,5],[7,8,9,10,11]],[[11,12,13,14,15],[17,18,19,20,21]]],
                [[[1,2,3,4,5],[7,8,9,10,11]],[[11,12,13,14,15],[17,18,19,20,21]]]])
print('x6 =', x6.shape)     # x6 = (3, 2, 2, 5) : 3x2x2x5 '텐서'
